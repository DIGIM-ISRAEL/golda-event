const OLD_PARVE = ['קוקוס','בננה','תמר','לימונצלו','אבטיח נענע','תות','פירות יער',"אוכמניות וליצ'י",'אננס','מנגו','טרופי','שוקולד מריר','בייגלה מריר','מוס קרם עוגיות טבעוני','מוס קרם עוגיות קרמל טבעוני']

async function main() {
  for (const [i, name] of OLD_DAIRY.entries()) {
    const exists = await db.flavor.findFirst({ where: { name, category: 'dairy' } })
    if (!exists) await db.flavor.create({ data: { name, category: 'dairy', sortOrder: i } })
  }
  for (const [i, name] of OLD_PARVE.entries()) {
    const exists = await db.flavor.findFirst({ where: { name, category: 'parve' } })
    if (!exists) await db.flavor.create({ data: { name, category: 'parve', sortOrder: i } })
  }
  // טעם זבל שאמור להימחק (לא בשום קטלוג, בלי שיבוצים)
  const junk = await db.flavor.findFirst({ where: { name: 'טעם ניסיון למחיקה' } })
  if (!junk) await db.flavor.create({ data: { name: 'טעם ניסיון למחיקה', category: 'dairy' } })

  console.log('old catalog seeded. total flavors:', await db.flavor.count())
}

main()
  .catch((e) => { console.error(e); process.exit(1) })
  .finally(() => db.$disconnect())
